def expand_along_BlockGemmPipelineVersion(configurations):
    BlockGemmPipelineVersion_list = [
        # "ck::BlockGemmPipelineVersion::v1", 
    "ck::BlockGemmPipelineVersion::v2"]
    opid_list = [int(config["id"]) for config in configurations]
    max_id = max(opid_list)


    new_configurations_list = []

    for BlockGemmPipelineVersion in BlockGemmPipelineVersion_list:
        for i in range(len(configurations)):
            new_config = copy.deepcopy(
                configurations[i]
            )
            new_config["id"] = max_id + i + 1
            new_config["BlockGemmPipelineVersion"] = BlockGemmPipelineVersion
            new_configurations_list.append(
                new_config
            )

    return configurations + new_configurations_list


operations = expand_along_BlockGemmPipelineVersion(operations)

